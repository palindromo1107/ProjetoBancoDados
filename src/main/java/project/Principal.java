    private void EditarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_EditarActionPerformed
        Produto p;
        if (linhaSelecionada != -1) {

            int id = (int) jTable1.getValueAt(linhaSelecionada, 0);
            String nome = jTable1.getValueAt(linhaSelecionada, 1).toString();
            int preco = (int) jTable1.getValueAt(linhaSelecionada, 2);
            Categoria c = (Categoria) jTable1.getValueAt(linhaSelecionada, 3);
            int estoque = (int) jTable1.getValueAt(linhaSelecionada, 4);

            System.out.println("Selecionado: " + nome);

            p = new Produto(id, nome, preco, estoque, c.getId(), c.getCategoria());

            edit.setProduto(p);
            edit.setVisible(true);
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecione algum produto para editar!");
            return;
        }
    }// GEN-LAST:event_EditarActionPerformed

