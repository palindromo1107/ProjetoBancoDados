    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_salvarActionPerformed
        Categoria c = (Categoria) Categoria.getSelectedItem();
        services.atualizarProduto(new Produto(ID, Nome.getText(), Integer.parseInt(Preco.getText()),
                Integer.parseInt(Estoque.getText()), c.getId(), c.getCategoria()));
        this.setVisible(false);
    }// GEN-LAST:event_salvarActionPerformed

    private void cancelarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_cancelarActionPerformed
        services.removerProduto(ID);
        this.setVisible(false);
    }// GEN-LAST:event_cancelarActionPerformed

    public void carregarCategorias() {
        try {

            List<Categoria> cat = categoriaServ.listarCategorias();

            Categoria.removeAllItems(); // limpa antes

            for (Categoria c : cat) {
                Categoria.addItem(c);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar categorias!");
            e.printStackTrace();
        }
    }

    private void selecionarCategoriaDoProduto() {
        for (int i = 0; i < Categoria.getItemCount(); i++) {
            Categoria c = Categoria.getItemAt(i);
            if (c.getId() == categoriaProduto) {
                Categoria.setSelectedIndex(i);
                break;
            }
        }
    }

    public void carregarInfo() {
        this.ID = produto.getId();
        Nome.setText(produto.getNome());
        Preco.setText(String.valueOf(produto.getValor()));
        Estoque.setText(String.valueOf(produto.getEstoque()));

        selecionarCategoriaDoProduto();

    }

    public void setProduto(Produto produto) {
        this.produto = produto;
        this.categoriaProduto = produto.getId_categoria();
        carregarCategorias();
        carregarInfo();
    }

